# Tipos Primitivos e Saída de Dados

n1 = int(input('Digite um valor: '))
n2 = int(input('Digite um valor: '))

soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
# print('O resultado é: {}'.format(soma))
print('A soma entre {} e {} é igual a {}.'.format(n1, n2, soma))

print('A subtração{} menos {} é igual a {}.'.format(n1, n2, subtracao))
print('A multiplicação entre {} e {} é igual a {}.'.format(n1, n2, multiplicacao))
print('A divisão entre {} e {} é igual a {}.'.format(n1, n2, divisao))
